# --- 4. Preprocesamiento de Texto (NLP_PREPROCESSOR) ---
def preprocess_text(text):
    """
    Realiza tokenizaci칩n, limpieza y lematizaci칩n del texto.
    Preserva c칩digos de curso y t칠rminos espec칤ficos.
    """
    # Convertir a min칰sculas
    text = text.lower()
    # Eliminar URLs (si las hubiera, aunque el PDF no las tiene expl칤citamente en el contenido)
    text = re.sub(r'http\S+|www\S+|https\S+', '', text)
    # Eliminar caracteres especiales, pero mantener n칰meros y letras
    text = re.sub(r'[^a-z치칠칤칩칰칲침\s\d\(\)-]', '', text) # Mantener par칠ntesis y guiones para c칩digos
    # Eliminar m칰ltiples espacios
    text = re.sub(r'\s+', ' ', text).strip()

    doc = nlp(text)
    lemmas = []
    for token in doc:
        # Conservar c칩digos de curso y niveles espec칤ficos como tokens 칰nicos
        if re.match(r'^\(?[a-z]{2,4}\d{1,2}-\d{1,2}\)?$', token.text) or \
           re.match(r'^(c\.p\.n\.[1-3]|gr\.c n\.[1-3]|grb치sico|grmedio|grsuperior|cursoespec)$', token.text) or \
           re.match(r'^\d+c$', token.text) or \
           token.text in ["diurno", "vespertino", "biling칲e:in", "nuevo", "extremadura", "badajoz", "c치ceres"]:
            lemmas.append(token.text)
        elif not token.is_stop and not token.is_punct and not token.is_space:
            lemmas.append(token.lemma_)
    return " ".join(lemmas)

